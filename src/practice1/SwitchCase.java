package practice1;

public class SwitchCase {
	public static void main(String[] args) {
		int number = 40;
		switch (number) {
		case 5:
			System.out.println("05");
			break;
		case 10:
			System.out.println("10");
			break;
		case 15:
			System.out.println("15");
			break;
		case 20:
			System.out.println("20");
			break;
		default:
			System.out.println("not 05,10,15,20");

		}
	}

}
